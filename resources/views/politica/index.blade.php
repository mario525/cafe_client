<h1 class="text-center text-lg"> ALCANCE DE ESTA POLÍTICA</h1>

    La presente Política explica cómo se recogen, usan y divulgan sus datos personales por parte de UNEDL Cafe. También le indica cómo puede acceder y actualizar sus datos personales y efectuar ciertas elecciones sobre cómo se utilizan sus Datos Personales.

     

    Esta Política engloba nuestras actividades de recogida de datos en línea.

     

     <h1 class="text-center text-lg">1. FUENTES DE DATOS PERSONALES</h1>



     

    Esta Política es de aplicación a los Datos Personales que recogemos de o sobre usted, a través de los métodos descritos a continuación (vea Sección 2), de las siguientes fuentes:

     

    Sitios web de Unedl.

     


     

    Correo electrónico, mensajes de texto u otros mensajes electrónicos. Comunicaciones electrónicas entre usted y Unedl Cafe.

     
     


     

    Datos de otras fuentes. Redes sociales y operadores de Internet (Facebook, Google, etc.) o información obtenida a través de investigaciones de mercado.

     

     
     <h1 class="text-center text-lg">2. DATOS PERSONALES QUE RECOGEMOS SOBRE USTED Y CÓMO LOS RECOGEMOS</h1>



     

    Dependiendo de cómo interactúa usted con Unedl Cafe (en línea, de forma presencial, por teléfono, etc.) recogemos varios tipos de información de usted, tal como se describe a continuación.

     

    Información de contacto personal. Esto incluye cualquier información que nos facilite que nos permita ponernos en contacto con usted, como su nombre, dirección postal, dirección de correo electrónico, detalles de redes sociales o número de teléfono.

     

    Información de registro de la cuenta. Cualquier información que se requiera para permitirle el acceso a su perfil de cuenta específico. Algunos ejemplos son su alias para la identificación de inicio de sesión, su dirección de correo electrónico, su nombre de usuario, su contraseña en una forma irrecuperable y/o la/s pregunta/s de seguridad y respuesta/s.

     

    Información demográfica. Cualquier información que describa sus características demográficas o de conducta. Algunos ejemplos son su fecha de nacimiento, edad o franja de edad, sexo, ubicación geográfica, productos favoritos, aficiones e intereses e información sobre el hogar y el estilo de vida.

     

    Datos Personales Sensibles. Nuestra actividad comercial ordinaria no tiene como objeto recoger o tratar datos personales sensibles. No obstante, como fabricantes de productos de alimentación infantil, debemos cumplir los requisitos legales para la comunicación y marketing de este tipo de productos. Te solicitamos la fecha prevista para el nacimiento de tu bebé y su sexo a fin de adecuar nuestras comunicaciones a la fase del embarazo en la que te encuentres o a la edad de tu bebé, de conformidad con la legislación aplicable y, concretamente, al Reglamento (EU) 609/2013, relativo a los alimentos destinados a los lactantes y niños de corta edad. Nos basamos en tu consentimiento expreso, voluntario y previo a cualquier tratamiento, para ofrecerte noticias, concursos y otras ofertas personalizadas. Si tratamos tus datos sensibles para otras finalidades, basaremos el tratamiento en las siguientes bases legales: (i) detección y prevención de delitos (incluyendo la prevención del fraude); y (ii) el cumplimiento de la legislación aplicable (i.e. cumplir con nuestro informe de diversidad).

     

    Información técnica sobre dispositivos informáticos/móviles. Toda información sobre el sistema informático u otro dispositivo tecnológico que usted utilice para acceder a nuestros Sitios web o apps, como la dirección de protocolo de Internet (IP) utilizada para conectar su ordenador o dispositivo a Internet, el tipo de sistema operativo y el tipo y la versión del navegador web. Si usted accede al sitio web, la información recogida también incluirá, cuando esté permitido, el identificador único de dispositivo de su teléfono, identificación de publicidad, ubicación geográfica y otros datos similares de dispositivos móviles.

     

    Datos de actividad e información usual sobre comunicaciones y visitas a sitios web. En el contexto de promociones y/o acciones comerciales en las que participe, podremos recoger información relativa a las compras realizadas a través de la información de sus tickets de compra o los cupones adquiridos o descargados de nuestras webs o redes sociales. Igualmente, mientras usted navegue e interactúe con nuestros Sitios web o boletines informativos, usamos tecnologías de recogida de datos automáticas para recoger determinada información sobre sus acciones. Esto incluye información como los vínculos en los que hace clic, las páginas o contenidos que usted visualiza y durante cuánto tiempo, y otra información y estadísticas similares sobre sus interacciones, como los tiempos de respuesta del contenido, errores de descarga y duración de las visitas a determinadas páginas. Esta información se captura utilizando tecnologías automatizadas como cookies (cookies de navegador, flash cookies) y balizas web y también se recoge mediante el uso de servicios de seguimiento de terceros. Usted tiene el derecho de oponerse al uso de dichas tecnologías. Para más detalles, consulte el Apartado 3.

     

    Investigaciones de mercado y comentarios de consumidores. Esto incluye información que usted comparte de forma voluntaria con Nosotros sobre su experiencia de uso de nuestros productos y servicios.

     

    Contenido generado por el consumidor. Se refiere a cualquier contenido que usted crea y luego comparte con Nosotros en una red social o mediante la carga en uno de nuestros Sitios web o apps, incluido el uso de las apps de redes sociales como Facebook. Algunos ejemplos incluyen fotos, vídeo, historias personales u otros medios o contenidos similares. Cuando se permite, recogemos y publicamos contenido generado por el consumidor en relación con una variedad de actividades, incluidos concursos y otras promociones, funciones de la comunidad del sitio web, eventos dirigidos al consumidor y redes sociales de terceros.

     


     

    Información de pagos y financiera. Cualquier información que necesitemos con el fin de satisfacer un pedido, o que usted utilice para realizar un pedido, como los detalles de la tarjeta de débito o crédito (nombre del titular, número de tarjeta, fecha de caducidad, etc.) u otras formas de pago (si se ponen a disposición). En todo caso, manejamos la información de pagos y financiera de conformidad con la leyes y reglamentos aplicables y los estándares de seguridad como el Estándar de Seguridad de Datos para la Industria de Tarjeta de Pago (Payment Card Industry Data Security Standard, PCI DSS).

     

    Llamadas al CRC. Las comunicaciones con un CRC podrán ser grabadas y listadas, conforme a las leyes aplicables, para responder a necesidades operacionales locales (p. ej. para fines formativos o de calidad) así como, en ciertos casos, disponer de trazabilidad del consentimiento prestado para el cumplimiento de determinadas finalidades. Los datos de la tarjeta de pago no se graban. Cuando se requiera por ley, se le informará sobre dicha grabación al inicio de su llamada y tendrá la oportunidad de objetar.

     

     
     <h1 class="text-center text-lg">3. COOKIES/TECNOLOGÍAS SIMILARES, ARCHIVOS DE REGISTRO y BALIZAS WEB</h1>


     

    Cookies/Tecnologías similares. Consulte nuestra Política de Cookies para saber cómo gestionar la configuración de sus cookies y para obtener información detallada sobre qué tipos de cookies utilizamos y con qué finalidades las usamos.

     

    Archivos de registro. Almacenamos información en forma de archivos de registro (“logs”) que registran la actividad web y recoge estadísticas sobre sus hábitos de navegación en Internet. Estas entradas se generan de forma automática y nos ayudan a identificar y solucionar problemas o errores, mejorar el cumplimiento y mantener la seguridad de nuestras páginas web.

     

    Balizas Web (Web Beacons). Las balizas web (conocidas también como “web beacons” o “web bugs”) son pequeñas secuencias de código que emiten una imagen gráfica en una página web o en un correo a fin de devolvernos información. Los datos recogidos a través de las balizas web incluyen información como la dirección IP, así como información sobre cómo responde usted a una campaña por correo electrónico (i.e. a qué hora abrió el correo, a qué enlaces accedió en el correo, etc.). Podremos utilizar balizas web en nuestras páginas o incluirlas en los correos electrónicos que le enviemos. Usamos la información obtenida a través de las balizas web para diversas finalidades, incluyendo, entre otras, informes de tráfico de páginas web, cuentas de visitante único, publicidad, auditorías e informes de correos electrónicos, y servicios de personalización.

     

     
     <h1 class="text-center text-lg">4. USOS DE LOS DATOS PERSONALES</h1>



     

    Los siguientes párrafos describen las distintas finalidades para las que recogemos y tratamos sus Datos Personales, así como las distintas tipologías de Datos Personales que se recogen para cada finalidad. Tenga en cuenta que no todas las finalidades indicadas a continuación tendrán la misma relevancia para cada interesado.

     

    Servicio al cliente. Utilizamos sus Datos Personales para finalidades relacionadas con la prestación de servicios a clientes, incluyendo las respuestas a sus solicitudes. Este tipo de servicios suelen conllevar el tratamiento de algunos datos de contacto, así como información relacionada con el contenido de la solicitud (i.e. estado del pedido, aspectos técnicos, quejas o dudas sobre el producto, preguntas generales, etc.).

     

    Concursos, marketing y otras promociones. Con su consentimiento (cuando sea preciso), tratamos sus Datos Personales para facilitarle información acerca de bienes y servicios (i.e. comunicaciones de marketing, campañas o promociones). Lo anterior puede realizarse a través del correo electrónico, anuncios, SMS, llamadas telefónicas o mediante correo postal, de conformidad con la legislación aplicable en cada momento. Algunas de nuestras campañas y promociones son gestionadas por webs de terceros y/o redes sociales. El uso de sus Datos Personales es voluntario, lo que implica que puede oponerse al tratamiento de sus Datos Personales (o retirar su consentimiento) para estas finalidades. Si necesita información más detallada sobre cómo modificar sus preferencias respecto de las comunicaciones de marketing, consulte los apartados 8 y 9, a continuación. Para más información sobre nuestros concursos y otras promociones, consulte la normativa oficial o los detalles que acompañen a cada concurso / promoción.

     

    Redes sociales: Utilizamos sus Datos Personales cuando usted interactúa en redes sociales de terceros, (i.e. utilizando funciones como “Me gusta”), para ofrecerle anuncios y dirigirnos a usted en redes sociales. Puede obtener más información sobre cómo funcionan estas funciones, los datos de perfil que obtenemos de usted, y conocer cómo excluir dichas funciones, accediendo a las políticas de privacidad de las principales redes sociales.

     


     

    Gestión de pedidos. Utilizamos sus Datos Personales para procesar y enviar sus pedidos, informarle del estado de los mismos, corregir direcciones y llevar a cabo verificaciones sobre su identidad y otras actividades para la detección del fraude. Lo anterior implica el tratamiento de determinados Datos Personales y de información sobre pagos.

     

    Otros propósitos generales (p. ej., estudios internos, análisis, seguridad). De conformidad con la legislación aplicable, utilizamos sus Datos para otras finalidades profesionales generales, incluyendo la realización de estudios demográficos o de marketing internos, o la medición de la efectividad de campañas publicitarias.

     


     

     


     
     <h1 class="text-center text-lg">5. CONSERVACIÓN DE SUS DATOS PERSONALES</h1>



     

    Conforme a las leyes aplicables, utilizaremos sus Datos Personales en la medida en que sean necesarios para satisfacer los propósitos para los que se recogieron (como se describe en la Sección 4 anterior) o para cumplir con los requisitos legales aplicables.

     

    Los Datos Personales utilizados para proporcionarle una experiencia personalizada (ver Sección 4 anterior para más detalles) se conservarán durante la duración permitida por las leyes aplicables.

     
     <h1 class="text-center text-lg">6. DIVULGACIÓN, ALMACENAMIENTO Y/O TRANSFERENCIA DE DATOS PERSONALES</h1>



     

    Utilizamos una variedad de medidas razonables (descritas a continuación) para mantener sus Datos Personales de forma confidencial y segura. No obstante, tenga en cuenta que esas protecciones no se aplican a la información que elija compartir en áreas públicas como redes sociales.

     

    Personas que pueden acceder a sus Datos Personales. Sus Datos Personales serán procesados por personal autorizado, en función de la necesidad de conocer, dependiendo de los fines específicos para los que sus Datos Personales se hubiesen recogido (p. ej. nuestro personal encargado de los servicios de atención al cliente tendrá acceso a su registro de cliente).

     

    Medidas tomadas en entornos operativos. Almacenamos sus Datos Personales en entornos operativos que utilizan medidas de seguridad razonables para prevenir el acceso no autorizado. Seguimos unos estándares razonables para proteger los Datos Personales. La transmisión de información a través de Internet no es, desafortunadamente, del todo segura, y aunque hacemos todo lo posible para proteger la información personal, no podemos garantizar la seguridad de los datos durante la transmisión mediante nuestros Sitios web/apps.

     

    Medidas que esperamos que usted tome. Es importante que usted también ponga de su parte para el mantenimiento seguro de sus Datos Personales. Al darse de alta en una cuenta en línea, asegúrese de escoger una contraseña de cuenta que sea difícil averiguar para los demás y nunca revele su contraseña a nadie. Usted es responsable de mantener de forma confidencial esta contraseña y para cualquier uso de su cuenta. Si usted utiliza un ordenador público o compartido, no escoja que se recuerde su identificación de inicio de sesión/dirección de correo electrónico o contraseña y asegúrese de cerrar sesión cada vez que abandone el ordenador. Debería también hacer uso de la configuración o controles de privacidad que Nosotros le proporcionamos en nuestro Sitio web/app.

     


     

     

     
     <h1 class="text-center text-lg">7. SUS DERECHOS SOBRE SUS DATOS PERSONALES</h1>



     

    Acceso a sus Datos Personales. Cuando la ley lo disponga, usted, sus sucesores, representantes y/o apoderados tienen el derecho de acceder, revisar y solicitar una copia física o electrónica de información almacenada sobre usted. Usted también podrá tener derecho a solicitar información sobre la fuente de sus Datos Personales.

     

    Modificación y supresión de sus Datos Personales. Cuando la ley lo disponga, usted, sus sucesores, representantes y/o apoderados podrán solicitar la corrección o modificación de sus Datos Personales. También tienen derecho a solicitar la supresión de sus Datos Personales para que dejen de tratarse, por decisión propia, si ya no son necesarios para los fines para los que fueron recogidos, o si son tratados de otro modo, especialmente si han retirado su consentimiento para el tratamiento, o se oponen a un determinado tratamiento.

     

    Tenga en cuenta que, en determinadas circunstancias, no podremos suprimir sus Datos Personales sin suprimir también su cuenta de usuario. Puede que se nos requiera conservar algunos de sus Datos Personales después de que haya solicitado la supresión, para cumplir nuestras obligaciones legales o contractuales o para satisfacer nuestras necesidades empresariales, todo ello de conformidad con la legislación aplicable.

     

    Limitación del tratamiento de sus Datos Personales. En determinadas circunstancias y cuando la ley lo disponga, usted, sus sucesores, representantes y/o apoderados podrán solicitar la limitación del tratamiento de sus datos por parte de Unedl Cafe a determinados fines, en cuyo caso únicamente los conservaremos para el ejercicio o la defensa de reclamaciones.

     

    Portabilidad de sus Datos Personales. En caso de que el tratamiento de Datos Personales esté basado en el consentimiento prestado por usted o cuando el tratamiento sea necesario para la ejecución de un contrato, usted podrá solicitar recibir en un formato estructurado, de uso común, de lectura mecánica e interoperable todos aquellos datos tratados a través de medios automatizado.

     

    Oposición al tratamiento de sus Datos Personales. En los casos en que sus Datos Personales sean tratados con la finalidad de satisfacer intereses legítimos de Unedl Cafe, en determinadas circunstancias, usted tendrá derecho a oponerse total o parcialmente (esto es, para determinadas finalidades) al tratamiento de cualquier dato personal relativo a su situación particular, y ello en cualquier momento y sin coste alguno.

     

    Revocación del consentimiento. En caso de que la base legitimadora para el uso de sus datos sea el consentimiento otorgado por usted de forma expresa o inequívoca, tiene derecho a retirar su consentimiento para que tratemos sus datos para uno, varios o todos los grupos de finalidades relacionados.

     

    Cuando esté disponible, nuestros Sitios web disponen de una función especializada mediante la cual usted puede revisar y modificar los Datos Personales que ha facilitado y revocar el consentimiento otorgado para uno, varios o todos los grupos de finalidades para los que lo hubiese otorgado. Tenga en cuenta que requerimos a nuestros consumidores registrados que verifiquen su identidad (p. ej. identificación de inicio de sesión/dirección de correo electrónico, contraseña) antes de que puedan acceder o realizar cambios a la información de su cuenta. Esto ayuda a prevenir el acceso no autorizado a su cuenta.

     

    Estos derechos pueden ejercerse por correo electrónico o postal o a través de nuestro servicio de atención al cliente, para lo cual le podremos solicitar que adjunte una copia de su documento oficial de identificación o detalles equivalentes (incluyendo mecanismos reforzados para acreditar su identidad cuando se lo solicitemos y no esté prohibido por la legislación). Si la solicitud la presenta una persona distinta de usted, sin aportar prueba de que la solicitud se ha efectuado legalmente en su nombre, la solicitud podrá ser rechazada.

     

    Tenga en cuenta que cualquier información de identificación que nos facilite se procesará únicamente conforme a las leyes aplicables y en la medida permitida por estas.

     

    Esperamos haber resuelto sus dudas respecto de cómo procesamos sus Datos Personales. No obstante, tiene derecho a presentar una queja ante las autoridades competentes en materia de protección de datos (Agencia Española de Protección de Datos) si considera que no hemos dado respuesta a alguna de sus consultas o si considera vulnerado alguno de sus derechos.

     

     

     
     <h1 class="text-center text-lg">8. SUS OPCIONES RESPECTO A CÓMO USAMOS Y DIVULGAMOS SUS DATOS PERSONALES</h1>



     

    Nos esforzamos en proporcionarle opciones relativas a los Datos Personales que usted nos facilitó. Los siguientes mecanismos le otorgan el siguiente control sobre sus Datos Personales:

     

    Cookies/tecnologías similares. Puede configurar su navegador para rechazar todas o algunas de las cookies del navegador, o para alertarle cuando las cookies se están usando. Consulte la Sección 3 anterior.


    Puedes dejar de recibir comunicaciones comerciales por correo electrónico o mensajes de texto a través de tu ordenador, tablet o dispositivo móvil siguiendo las instrucciones que te proporcionaremos en cada comunicación. Si dispones de una cuenta con nosotros, puedes cambiar tus preferencias de privacidad o de contacto en la sección correspondiente, o contactando con nuestro servicio de atención. El cambio de estas preferencias eliminaría tus datos de nuestras listas para futuras acciones de publicidad dirigida. También puedes visitar los sitios de los buscadores o las redes sociales de las que eres miembro y modificar las opciones de seguridad para no recibir publicidad dirigida.

     
     <h1 class="text-center text-lg">9. CAMBIOS A NUESTRA POLÍTICA</h1>



     

    Si cambiamos la forma en que procesamos Datos Personales, actualizaremos esta Política y el correspondiente Aviso Legal de Privacidad y Protección de Datos. Nos reservamos el derecho a realizar cambios en cualquier momento a nuestras prácticas y a esta Política, por lo que le instamos a consultarla con frecuencia para comprobar si hay actualizaciones o cambios.

     
